num = 5 #num คือกำหนดตัวเลข 
x = '' #x คือ ''
while num > 0: #การทำงาน แปลงฐาน 10 เป็นฐาน 2
    r = num % 2 #หาเลขฐาน 2
    num = num // 2 #หารไม่เอาเศษ
    x += str(r) #แปลงเป็นตัวอักษรstr
print(x[::-1]) #แสดงข้อมูลให้อยู่ในบรรทัดเดียวกัน ::
if x == x[ :: -1]:#ถ้าใช่ เช็คตัวเลขที่กลับแล้วจะมีค่าเท่ากัน
    print('Is Palindrome') #เป็นPalindrome
else: #ถ้าไม่ใช่
    print('Is Not Palindrome') #ไม่ใช่Palindrome